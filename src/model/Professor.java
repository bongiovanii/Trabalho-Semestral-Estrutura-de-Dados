package model;

public class Professor {
	 private long cpf;
	 private String nome;
	 private String area;
	 private long pontos;
	public Professor() {
		super();
	}
		
	public long getCpf() {
		return cpf;
	}
	public void setCpf(long cpf) {
		this.cpf = cpf;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public long getPontos() {
		return pontos;
	}
	public void setPontos(long pontos) {
		this.pontos = pontos;
	}
	 
	 
	 
}
